int route[NODE];

void displayroute() {
   cout<<"Yes! There exist a route for this Icosian graph:";

   for (int i = 0; i < NODE; i++)
      cout << route[i] << " ";
   cout << route[0] << endl;     
}

bool isValid(int v, int k) {
   if (graph [route[k-1]][v] == 0)   
      return false;

   for (int i = 0; i < k; i++)   
      if (route[i] == v)
         return false;
   return true;
}

bool cycleFound(int k) {
   if (k == NODE) {             
      if (graph[route[k-1]][ route[0] ] == 1 )
         return true;
      else
         return false;
   }

   for (int v = 1; v < NODE; v++) {       
      if (isValid(v,k)) {                
         route[k] = v;
         if (cycleFound (k+1) == true)
            return true;
         route[k] = -1;               
      }
   }
   return false;
}

bool icosianroute() {
   for (int i = 0; i < NODE; i++)
      route[i] = -1;
   route[0] = 0; 

   if ( cycleFound(1) == false ) {
      cout << "No route possible"<<endl;
      return false;
   }

   displayroute();
   return true;
}

